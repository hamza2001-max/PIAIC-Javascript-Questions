const age = 20;

if (age < 2) { console.log("person is a baby"); }
if (age >= 2 && age < 4) { console.log("person is a toddler"); }
if (age >= 4 && age < 13) { console.log("person is a kid"); }
if (age >= 13 && age < 20) { console.log("person is a teenager"); }
if (age >= 20 && age < 65) { console.log("person is a adult"); }
if (age >= 65) { console.log("person is a elder"); }